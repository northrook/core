<?php

include __DIR__ . '/../vendor/autoload.php';

/** @var array<array-key,string> $identifiers */
$identifiers = DateTimeZone::listIdentifiers();

/** @var array<string,string> $timezones */
$timezones = [];
$longest   = 9;

foreach ($identifiers as $timezone) {
    $enumCase = 'case '
    . str_replace(
        ['/', '-'],
        '_',
        strtoupper($timezone),
    );

    if (strlen($enumCase) > $longest) {
        $longest = strlen($enumCase);
    }

    $timezones[$timezone] = $enumCase;
}

foreach ($timezones as $identifier => $enumCase) {
    $timezones[$identifier] = TAB . str_pad($enumCase, $longest) . " = '" . $identifier . "';";
}

$cases = implode("\n", $timezones);
$enum  = <<<PHP
    <?php // Auto-generated by ./bin/generate-timezone-enum.php

    declare(strict_types=1);

    namespace Northrook\Core\DateTime;

    use Northrook\Core;
    use DateTimeZone;
    use Exception;

    enum TimeZone: string 
    {

    public const null AUTO = null;

    {$cases}

        public function resolve(): DateTimeZone
        {
            try {
                return new DateTimeZone(\$this->value);
            } catch (Exception \$e) {
                // This should be impossible, as all cases are from DateTimeZone::listIdentifiers
                // Will add phpstan ignore exception
                Core::log()->error(
                    "Failed to resolve timezone: {\$this->value} - {\$e->getMessage()}",
                    ['exception' => \$e],
                );
                return new DateTimeZone('UTC');
            }
        }
    }

    PHP;

file_put_contents(__DIR__ . '/../src/Core/DateTime/TimeZone.php', $enum);
